<script>
    document.addEventListener('DOMContentLoaded', function() {
        let animationTriggered = false; // To ensure animations only play once per section entry

        // Function to reset all animations (for re-triggering on scroll out/in if needed)
        function resetAnimations() {
            // Reset section title/description animations
            document.querySelectorAll('.section-title, .section-divider, .section-description').forEach(el => {
                el.classList.remove('is-visible');
            });

            // Reset skill card animations
            document.querySelectorAll('.skill-card').forEach(card => {
                card.classList.remove('is-visible');
                // Reset skill bars
                card.querySelectorAll('.skill-bar').forEach(bar => {
                    bar.style.width = '0%';
                });
            });
            animationTriggered = false;
        }

        // Function to trigger title and card animations
        function triggerAnimations() {
            if (animationTriggered) return; // Only trigger once per intersection
            animationTriggered = true;

            // Trigger section title/description animations
            const titleElements = document.querySelectorAll(
                '.section-title, .section-divider, .section-description');
            titleElements.forEach((el, index) => {
                setTimeout(() => {
                    el.classList.add('is-visible');
                }, index * 200); // Stagger title elements
            });

            // Trigger skill card animations sequentially
            const skillCards = document.querySelectorAll('.skill-card');
            const sortedCards = Array.from(skillCards).sort((a, b) => {
                return parseInt(a.dataset.animationOrder) - parseInt(b.dataset.animationOrder);
            });

            sortedCards.forEach((card, index) => {
                setTimeout(() => {
                    card.classList.add('is-visible');
                    // Once card is visible, animate its skill bars
                    const skillBars = card.querySelectorAll('.skill-bar');
                    skillBars.forEach((bar, barIndex) => {
                        setTimeout(() => {
                            const level = bar.dataset.level;
                            bar.style.width = level + '%';
                        }, barIndex * 150); // Stagger skill bars within a card
                    });
                }, 400 + index * 100); // Initial delay + staggered delay for each card
            });
        }

        // Intersection Observer for section entrance animations
        const skillsSection = document.getElementById('skills');
        const observerOptions = {
            root: null, // relative to the viewport
            rootMargin: '0px',
            threshold: 0.2 // Trigger when 20% of the section is visible
        };

        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    // Section is in view
                    triggerAnimations();
                } else {
                    resetAnimations();
                }
            });
        }, observerOptions);

        if (skillsSection) {
            observer.observe(skillsSection);
        }
    });
</script>
